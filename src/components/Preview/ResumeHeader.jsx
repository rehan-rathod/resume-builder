export default function ResumeHeader({ personal }) {
  const {
    fullName = '',
    jobTitle = '',
    email = '',
    phone = '',
    location = '',
    linkedin = '',
    github = '',
    website = '',
  } = personal || {};

  const contacts = [
    { type: 'email', value: email },
    { type: 'phone', value: phone },
    { type: 'location', value: location },
    { type: 'linkedin', value: linkedin ? `linkedin.com/in/${linkedin}` : '' },
    { type: 'github', value: github ? `github.com/${github}` : '' },
    { type: 'website', value: website },
  ].filter((c) => c.value);

  return (
    <header className="resume-header">
      <div className="resume-name">{fullName || 'Your Name'}</div>
      <div className="resume-title">{jobTitle || 'Your Job Title'}</div>
      <div className="resume-contacts">
        {contacts.map((c, i) => (
          <span key={i} className="resume-contact-item">
            <ContactIcon type={c.type} />
            {c.value}
          </span>
        ))}
      </div>
    </header>
  );
}
